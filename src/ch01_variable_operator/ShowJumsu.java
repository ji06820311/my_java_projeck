package ch01_variable_operator;

public class ShowJumsu {
    public static void main(String[] args) {
        String name;    //변수 정의
        double kor;
        double eng;
        double ave;

        name = "홍길동"; //변수 편입
        kor = 40.0;
        eng = 43.0;
        ave = (kor + eng) /2 ;

        System.out.println("이름 : " + name);   //변수값 결과 츨력
        System.out.println("국어 : " + kor);
        System.out.println("영어 : " + eng);
        System.out.println("평균 : " + ave);







    }
}
