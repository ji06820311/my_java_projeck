package ch01_variable_operator;

public class Add2 {
    public static void main(String[] args) {
        int a;      //정수(int) 정의
        int b;
        int c;
        int d;

        a = 3;  //정수(int) 대입
        b = 4;
        c = 5;
        d = 2 * a + 3 * b - c;

        System.out.println("a = 3");  // 정수(int) 그니깐 변수 값 출력
        System.out.println("b = 4");
        System.out.println("c = 5");
        System.out.println("result = " + d);










    }
}
